package com.oa.pojo;

public class AttendCount {
	private int unsign;
	private long late;
	private long earlyLeave;
	public int getUnsign() {
		return unsign;
	}
	public void setUnsign(int unsign) {
		this.unsign = unsign;
	}
	public long getLate() {
		return late;
	}
	public void setLate(long late) {
		this.late = late;
	}
	public long getEarlyLeave() {
		return earlyLeave;
	}
	public void setEarlyLeave(long earlyLeave) {
		this.earlyLeave = earlyLeave;
	}
	
}
